  <p>
    When discussing basketball’s greatest of all times, 2 names are often brought up: Michael Jordan and LeBron James.<br><br> 
    Before James, Jordan was the undisputed best basketball player to grace the court. In Jordan’s 13 years of playing basketball, he has made 6 finals and won all 6 of them, with both of them being a 3-peat. Not only did he win 6 out of 6 finals, he was also named the Finals MVP in all of them and won multiple MVPs during this time.<br><br> 
    In recent years, James has been in the conversation for the greatest basketball player ever. As of 2024, James has played for 21 years and won 4 of the 10 finals he made. Amongst elite NBA players, LeBron has had one of the longest careers, with the longest being Vince Carter’s career lasting 22 years. 
    While some people value Jordan’s 3-peats and 6 NBA rings, others value James’ longevity and his consistent high level of performance across all 21 years, with an average of 27.1 points per game. In those 21 years, his worst year was an impressive 20 points per game in his rookie season, and his best year was 31.4 points per game in his 2005-06 season.<br><br> 
    In both of their careers, it is undeniable that both players were key towards their teams’ success and that those teams wouldn’t have won those titles without them. In our website, we will compare the in-game statistics between these 2 GOAT candidates to weigh their contributions to their team.

